export function erpCommonT(
  key: string,
  lang: AppLanguage,
  vars?: Record<string, string | number>
): string {
  const row = strings[key]
  if (!row) return key
  let s = pick(lang, row.en, row.bn)
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
    }
  }
  return s
}

export function useErpCommonT() {
  const { language } = useCompanyLocale()
  return (key: string, vars?: Record<string, string | number>) => erpCommonT(key, language, vars)
}
